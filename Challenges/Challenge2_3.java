/*
Create a new Java class called "Challenge2_3.java".

Create two variables with a number each one.

Print the result of making an addition, a substraction, a multiplication and a division of this two variables in the screen.
 */
public class Challenge2_3 {

    public static void main(String[] args) {
        int a = 3;
        int b = 4;
     
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
    }
}
